                <h5>The Project</h5>

                <p>Cognitive Ease is quite simply the ease with which our brain processes information; this quality
                    impacts how positively (or negatively) we feel about something.
                </p>

                <p>We are more likely to let our guard down when we experience cognitive ease - the information
                    conveyed could feel more familiar and believable because of the way it has been presented. A visual
                    aid is presented below:
                </p>

                <img src="{{ asset('images/cognitive-ease-factors.png') }}"
                     class="img-fluid rounded border border-1 mb-3"
                     alt="Causes and Consequences of Cognitive Ease">

                <p>This project serves as an extendable testing ground for various ways in which Cognitive Ease can be
                    harnessed to influence user decisions.
                </p>

                <p>Causal factors of cognitive ease which have been utilized in this research
                    include:</p>

                <ul>
                    <li>
                        <strong>Clear Display:</strong> Maximizing contrast of text on background for one choice in
                        relation to others.
                    </li>
                    <li><strong>Mere Exposure Effect:</strong> Arguably a combination of <em>'Repeated Experience'</em>
                        and <em>'Primed Idea'</em>, you may have noticed a brief display of one of the choices for the
                        final question thorugh out the trivia.</li>
                </ul>





